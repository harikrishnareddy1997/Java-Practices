public class SwitchStatement {
	public static void main(String[] args) {

		char color = 'g';
		switch (color) {
		case 'r':
			System.out.println("color is red");
			break;
		case 'g':
			System.out.println("color green");
			break;
		case 'b':
			System.out.println("color blue");
			break;
		}

	}

}
